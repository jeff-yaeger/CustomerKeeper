        [HttpGet]
        [AllowAnonymous]
        [Route("{id:int}")]
        public IActionResult CustomerEdit([FromRoute]int id)
        {
            try
            {
                if (!ModelState.IsValid)
                {
                    return BadRequest(ModelState);
                }
                AppointDom uptmodel = new AppointDom();
                uptmodel = _svc.GetById(id);

                if (uptmodel == null)
                {
                    return NotFound();
                }
                //seperate date
                string res1 = uptmodel.Appoint.ToString("yyyy-MM-dd");
                string res2 = uptmodel.Appoint.ToString("HH:mm");

                //build model
                CustomerEditModel model = new CustomerEditModel();
                model.Id = uptmodel.Id;
                model.UserId = uptmodel.UserId;
                model.CustFName = uptmodel.CustFName;
                model.CustLName = uptmodel.CustLName;
                model.Street = uptmodel.Street;
                model.City = uptmodel.City;
                model.State = uptmodel.State;
                model.Zip = uptmodel.Zip;
                model.Email = uptmodel.Email;
                model.Phone = uptmodel.Phone;
                model.AppointDate = res1;
                model.AppointTime = res2;
                model.ModifiedBy = uptmodel.ModifiedBy;
                model.IsCnfrmed = uptmodel.IsCnfrmed;
                model.ReminderSent = uptmodel.ReminderSent;
                model.CompName = uptmodel.CompName;
                model.CompEmail = uptmodel.CompEmail;

                return View(model);
            }
            catch (Exception ex)
            { 
                return StatusCode(457, ex);
            }
        }

        [HttpPost]
        [AllowAnonymous]
        [Route("{id:int}")]
        public IActionResult CustomerEdit([FromRoute]int id, CustomerEditModel model)
        {
            try
            {
                if (ModelState.IsValid)
                {
                    //build model
                    AppointUpt uptmodel = new AppointUpt();
                    uptmodel.Id = model.Id;
                    uptmodel.UserId = model.UserId;
                    uptmodel.CustFName = model.CustFName;
                    uptmodel.CustLName = model.CustLName;
                    uptmodel.Street = model.Street;
                    uptmodel.City = model.City;
                    uptmodel.State = model.State.ToUpper();
                    uptmodel.Zip = model.Zip;
                    uptmodel.Email = model.Email;
                    uptmodel.Phone = model.Phone;
                    DateTime newDate = DateTime.Parse(model.AppointDate + "T" + model.AppointTime);
                    uptmodel.Appoint = newDate;
                    uptmodel.ModifiedBy = model.CustFName + " " + model.CustLName;
                    uptmodel.IsCnfrmed = model.IsCnfrmed;
                    uptmodel.ReminderSent = model.ReminderSent;
                    uptmodel.CompName = model.CompName;
                    uptmodel.CompEmail = model.CompEmail;

                    _svc.Put(id, uptmodel);
                    return RedirectToAction("DoneEdit", "Account");
                }
                else
                {
                    return BadRequest(ModelState);
                }
            }
            catch (Exception ex)
            {
                return StatusCode(420, ex);
            }
        }

